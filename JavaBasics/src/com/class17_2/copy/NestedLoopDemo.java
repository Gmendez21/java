package com.class17_2.copy;

public class NestedLoopDemo {
	
	public static void main(String[] args) {
		
//	24 hours, 60 minutes
	
		for (int h=0; h<60; h++) {
			
			for (int m=0; m<60; m++) { 
				
				if (m<=10) {
				
			System.out.println(h+":"+m);
				} else {
			System.out.println(h+":"+m);
		}
	
		}
	

}
}}